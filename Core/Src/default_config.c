#include <stdio.h>

#include "utils.h"

void generate_default_endpoint_name(char *endpoint_name, size_t len) {
    device_id_t id;
    get_uid(&id);
    snprintf(endpoint_name, len, "anjay-freertos-demo-%s", id.value);
}
